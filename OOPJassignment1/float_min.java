/*Write a program to find minimum and maximum number as well as to add two float numbers using methods of Float.*/

public class float_min {
    public static void main(String args[])
    {
        float min1 = Float.min(23.0f,26.05f);
        float max1 = Float.max(123.27f,125.76f);
        float sum1 = Float.sum(40.04f,60.06f);
System.out.println("min value = "+min1+"  "+"max value = "+max1+"  "+"sum = "+sum1);
    }
    
}
